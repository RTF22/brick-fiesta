// Geschwindigkeit pro Level (skaliert sanft). Basis 1.0, +1.5% pro Level.
function ballSpeedForLevel(n) {
  return 1.0 + (n - 1) * 0.015;
}

// Schläger-Breite-Skalierung (wird mit Powerups kombiniert): leicht schrumpfend ab Level 20.
function paddleScaleForLevel(n) {
  if (n < 20) return 1.0;
  if (n < 40) return 0.9;
  return 0.8;
}
